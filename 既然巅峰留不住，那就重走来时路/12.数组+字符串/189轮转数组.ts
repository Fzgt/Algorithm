// 189. 轮转数组
function rotate(nums: number[], k: number): void {
    k = k % nums.length
    for (let i = 0; i < k; i++) {
        const element = nums.pop();
        nums.unshift(element)
    }
};

// 为什么要%k就可以得到正确的翻转个数
// 假设数组长度是 n = 7（比如 [1,2,3,4,5,6,7]）：

// 向右轮转 7 次（等于数组长度），数组会完全变回原样，相当于没转
// 向右轮转 8 次 = 轮转 7 次（回到原样）+ 再轮转 1 次 = 效果等同于只轮转 1 次
// 向右轮转 10 次 = 轮转 7 次（回到原样）+ 再轮转 3 次 = 效果等同于只轮转 3 次

// 也就是说，多轮转 n 次（一整圈）等于没转，所以只有 k 除以 n 的余数才是"真正有效"的轮转次数。

// 比如 nums.length = 7：

// k = 3 → 3 % 7 = 3（不变，因为本来就小于长度）
// k = 7 → 7 % 7 = 0（转一整圈=没转）
// k = 10 → 10 % 7 = 3（转了一圈多3步，等价于只转3步）
// k = 17 → 17 % 7 = 3（转了两圈多3步，同样等价于转3步）